def get_hand_class(strength: int) -> int:
    """Convert hand strength to hand class (0-8)."""
    if strength >= HANDCLASS_STRAIGHT_FLUSH:
        return 8
    elif strength >= HANDCLASS_FOUR_OF_A_KIND:
        return 7
    elif strength >= HANDCLASS_FULL_HOUSE:
        return 6
    elif strength >= HANDCLASS_FLUSH:
        return 5
    elif strength >= HANDCLASS_STRAIGHT:
        return 4
    elif strength >= HANDCLASS_THREE_OF_A_KIND:
        return 3
    elif strength >= HANDCLASS_TWO_PAIR:
        return 2
    elif strength >= HANDCLASS_PAIR:
        return 1
    else:
        return 0

